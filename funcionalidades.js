let monedaActual = "MXN";

let tasasDeCambio = {
    "MXN": 1,
    "USD": 0.060,
    "EUR": 0.054,
    "CAD": 0.080
};

async function obtenerTasasEnVivo() {
    try {
        const respuesta = await fetch('https://open.er-api.com/v6/latest/MXN');
        const datos = await respuesta.json();

        if (datos && datos.rates) {
            tasasDeCambio = {
                "MXN": 1,
                "USD": datos.rates.USD,
                "EUR": datos.rates.EUR,
                "CAD": datos.rates.CAD
            };
        }
    } catch (error) {
        console.warn("Usando tasas de cambio de respaldo.");
    }
}

obtenerTasasEnVivo();

function toggleCurrencyMenu() {
    document.getElementById('currency-menu').classList.toggle('show');
}

function selectCurrency(moneda, flagCode) {
    document.getElementById('current-currency-text').textContent = moneda;
    document.getElementById('current-currency-flag').src = `https://flagcdn.com/w20/${flagCode}.webp`;

    document.getElementById('currency-menu').classList.remove('show');

    monedaActual = moneda;
    const modal = document.getElementById('modal');
    if (modal && modal.style.display === 'block') {
        updateSlideInfo(currentSlide);
    }
}

document.addEventListener('click', function (e) {
    const selector = document.querySelector('.custom-currency-selector');
    if (selector && !selector.contains(e.target)) {
        document.getElementById('currency-menu').classList.remove('show');
    }
});

let currentCategory = 'clasica';
let currentSlide = 0;
let totalSlides = 0;

function openModal(category) {
    currentCategory = category;
    currentSlide = 0;

    const data = catrinasData[category];
    if (!data) {
        console.error('Categoría no encontrada:', category);
        alert('Esta categoría no está disponible en este momento.');
        return;
    }

    const modal = document.getElementById('modal');
    totalSlides = data.slides.length;

    document.getElementById('modalTitle').textContent = data.title;

    const slidesContainer = document.getElementById('carouselSlides');
    slidesContainer.innerHTML = '';

    data.slides.forEach((slide, index) => {
        const slideDiv = document.createElement('div');
        slideDiv.className = `carousel-slide ${index === 0 ? 'active' : ''}`;

        slideDiv.innerHTML = `
            <img src="${slide.image}" 
                alt="${slide.altText}"
                onerror="this.src='fotos/catrin_portada.webp'">
        `;
        slidesContainer.appendChild(slideDiv);
    });

    updateSlideInfo(0);

    updateDots();

    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';

    setTimeout(() => {
        const images = slidesContainer.querySelectorAll('img');
        images.forEach(img => {
            if (!img.complete) {
                img.onload = () => {
                };
                img.onerror = () => {
                    img.src = 'fotos/catrin_portada.webp';
                };
            }
        });
    }, 100);
}

function closeModal() {
    document.getElementById('modal').style.display = 'none';
    document.body.style.overflow = 'auto';
}

function updateSlideInfo(index) {
    const data = catrinasData[currentCategory];
    const slide = data.slides[index];

    if (!slide) return;

    document.getElementById('pieceTitle').textContent = data.title;
    document.getElementById('pieceDescription').textContent = slide.description;
    document.getElementById('pieceMaterial').textContent = slide.material;
    document.getElementById('pieceHeight').textContent = slide.height;
    document.getElementById('pieceTechnique').textContent = slide.technique;
    document.getElementById('pieceOrigin').textContent = slide.origin;

    const tasa = tasasDeCambio[monedaActual] || 1;

    const formateador = new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: monedaActual,
        currencyDisplay: 'narrowSymbol'
    });

    let precioFinal = "";

    if (slide.priceMax) {
        const minFormat = formateador.format(slide.price * tasa);
        const maxFormat = formateador.format(slide.priceMax * tasa);
        precioFinal = `${minFormat} - ${maxFormat} ${monedaActual}`;
    } else if (slide.isDesde) {
        const precioFormat = formateador.format(slide.price * tasa);
        precioFinal = `Desde ${precioFormat} ${monedaActual}`;
    } else {
        const precioFormat = formateador.format(slide.price * tasa);
        precioFinal = `${precioFormat} ${monedaActual}`;
    }

    document.getElementById('piecePrice').textContent = precioFinal;

    const availabilityElement = document.getElementById('pieceAvailability');
    availabilityElement.textContent = slide.available ? 'Disponible' : 'Vendida';
    availabilityElement.className = slide.available ? 'available' : 'sold';
}

function showSlide(index) {
    if (index < 0) index = totalSlides - 1;
    if (index >= totalSlides) index = 0;

    currentSlide = index;

    const slides = document.querySelectorAll('.carousel-slide');
    slides.forEach(slide => slide.classList.remove('active'));

    if (slides[index]) {
        slides[index].classList.add('active');
    }

    updateSlideInfo(index);

    updateDots();
}

function nextSlide() {
    showSlide(currentSlide + 1);
}

function prevSlide() {
    showSlide(currentSlide - 1);
}

function updateDots() {
    const dotsContainer = document.getElementById('carouselDots');
    dotsContainer.innerHTML = '';

    for (let i = 0; i < totalSlides; i++) {
        const dot = document.createElement('div');
        dot.className = `dot ${i === currentSlide ? 'active' : ''}`;
        dot.onclick = () => showSlide(i);
        dotsContainer.appendChild(dot);
    }
}

function toggleMenu() {
    const menu = document.getElementById('menu');
    const toggle = document.querySelector('.menu-toggle');
    menu.classList.toggle('active');
    toggle.classList.toggle('active');
}

document.addEventListener('keydown', (e) => {
    const modal = document.getElementById('modal');
    if (modal && modal.style.display === 'block') {
        if (e.key === 'Escape') {
            closeModal();
        }
        if (e.key === 'ArrowLeft') {
            prevSlide();
        }
        if (e.key === 'ArrowRight') {
            nextSlide();
        }
    }
});

document.getElementById('modal').addEventListener('click', (e) => {
    if (e.target.classList.contains('modal-overlay')) {
        closeModal();
    }
});

document.querySelectorAll('nav a, .footer-menu a').forEach(link => {
    link.addEventListener('click', (e) => {
        e.preventDefault();

        const targetId = link.getAttribute('href');
        if (targetId.startsWith('#')) {
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                if (window.innerWidth <= 768) {
                    toggleMenu();
                }

                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        } else if (link.getAttribute('target') === '_blank') {
            window.open(link.getAttribute('href'), '_blank');
        } else {
            window.location.href = link.getAttribute('href');
        }
    });
});

document.addEventListener('click', (e) => {
    const menu = document.getElementById('menu');
    const toggle = document.querySelector('.menu-toggle');

    if (window.innerWidth <= 768 &&
        menu.classList.contains('active') &&
        !menu.contains(e.target) &&
        !toggle.contains(e.target)) {
        toggleMenu();
    }
});

